// Viết chương trình in ra tổng, tích, hiệu, thương của 2 số thực nhập từ bàn phím

#include <iostream>

using namespace std;

int main() {
	double a, b;
	cout << "a = "; cin >> a;
	cout << "b = "; cin >> b;

	cout << "a + b = " << a + b << endl;
	cout << "a - b = " << a - b << endl;
	cout << "a * b = " << a * b << endl;
	cout << "a / b = " << a / b << endl;

	return 0;
}
